// Supported languages
enum class AppLanguage(val code: String, val displayName: String) {
    SPANISH("es", "Español (Colombia)"),
    ENGLISH("en", "English")
}

// Global state for the current language — survives recomposition
object LanguageState {
    val current = mutableStateOf(AppLanguage.SPANISH)
}

// CompositionLocal to propagate language through the tree
val LocalAppLanguage = compositionLocalOf { AppLanguage.SPANISH }

// All app strings in both languages
object AppStrings {
    fun get(lang: AppLanguage): Strings = when (lang) {
        AppLanguage.SPANISH -> SpanishStrings
        AppLanguage.ENGLISH -> EnglishStrings
    }
}

